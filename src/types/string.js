var userName = 'jonasilken';

console.log('username:', userName);

// append operation (concatenation)
console.log('username: ' + userName);

// string literals
console.log(`username: ${userName}`);

/**
 * + -> concatenation
 * === -> equality
 */

console.log(userName === 'jonasilken');

userName = userName + '0'; // userName += '0';

console.log('username:', userName);

console.log(userName === 'jonasilken');
