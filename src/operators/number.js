const numberOne = 99;
const numberTwo = 98;

/**
 * Number Operators
 *
 * sum: +
 * sub: -
 * mult: *
 * div: /
 * module: %
 * power: **
 */

console.log(`${numberOne} + ${numberTwo}`, numberOne + numberTwo);
console.log(`${numberOne} - ${numberTwo}`, numberOne - numberTwo);
console.log(`${numberOne} * ${numberTwo}`, numberOne * numberTwo);
console.log(`${numberOne} / ${numberTwo}`, numberOne / numberTwo);
console.log('5 % 2', 5 % 2);
console.log('5 ** 2', 5 ** 2);
